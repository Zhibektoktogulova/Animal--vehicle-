package day04_concatenation;

public class PersonalInfo {

    public static void main(String[] args) {

        String firstName = "Alisa",
                 lastName = "Joshua";
        char gender = 'F';
        int age  = 20;
        String from = "USA",
                favoriteMusic = "\"Jazz\"",
                favoriteBook = "\"Outlier\"",
                favoriteShow = "\"Sunset Lover\"",
                favoriteColor = "Black";
        boolean isMarried = true,
                isEmployed = false;

        System.out.println("firstName = " + firstName);
        System.out.println("lastName = " + lastName);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("from = " + from);
        System.out.println("favoriteMusic = " + favoriteMusic);
        System.out.println("favoriteBook = " + favoriteBook);
        System.out.println("favoriteShow = " + favoriteShow);
        System.out.println("favoriteColor = " + favoriteColor);
        System.out.println("isMarried = " + isMarried);
        System.out.println("isEmployed = " + isEmployed);






    }
}
